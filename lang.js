function applyLang(lang) {
  const t = translations[lang];
  if (!t) return;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });
  // Update html lang attribute
  document.documentElement.lang = lang === 'fr' ? 'fr' : 'en';
  // Update lang switcher buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('font-bold', btn.getAttribute('data-lang') === lang);
    btn.classList.toggle('text-on-surface', btn.getAttribute('data-lang') === lang);
    btn.classList.toggle('text-on-surface-variant', btn.getAttribute('data-lang') !== lang);
  });
  localStorage.setItem('volt-lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {
  const savedLang = localStorage.getItem('volt-lang') || 'fr';
  applyLang(savedLang);
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => applyLang(btn.getAttribute('data-lang')));
  });
});
